"""OGDF layout adapter.

OGDF (Open Graph Drawing Framework, https://ogdf.uos.de/) is a C++ library
with extensive layout support, including high-quality orthogonal layouts via
``PlanarizationLayout`` and a hierarchical fallback (``SugiyamaLayout``) for
graphs that cannot be planarised.  This adapter drives OGDF through
``ogdf-python`` (cppyy-based), loading the locally built ``libOGDF.so`` /
``libCOIN.so`` from ``benchmarks/xray_layout/third_party/ogdf/build``.

Decision D1 (orthogonal-first with fallback):

* ``PlanarizationLayout`` is the primary engine -- it produces orthogonal
  routes with bend-minimisation, which best matches xray's existing visual
  contract.  If the graph is non-planar (or otherwise rejected by the
  planarity-based pipeline) we catch the C++/cppyy exception and fall back
  to ``SugiyamaLayout``, which always succeeds for general digraphs at the
  cost of a different visual style (hierarchical instead of orthogonal).
* The notes field ``layout_engine`` records which engine actually ran:
  ``"ortho"`` for planarisation, ``"sugiyama_fallback"`` when we degraded.

Library bootstrap notes (kept inside the adapter so the module stays
self-contained):

* ``ogdf-python`` resolves headers and shared libraries from the env vars
  ``OGDF_BUILD_DIR`` / ``OGDF_INSTALL_DIR``; we set ``OGDF_BUILD_DIR`` lazily
  before the first import so users do not need to export anything.
* ``LD_LIBRARY_PATH`` must include the OGDF build dir before cppyy's dlopen
  fires; we prepend it from the same lazy bootstrap.
* All OGDF algorithm objects we hold from Python (``PlanarizationLayout``,
  ``SugiyamaLayout``) have ``__python_owns__ = False`` set so cppyy does not
  attempt to free them: OGDF's destructors are not safe to call from the
  Python finaliser path because cppyy may collect the C++ object after the
  ``GraphAttributes`` it co-owns has already been torn down, which leads to
  a double-free crash on interpreter shutdown.
"""

from __future__ import annotations

import os
import time
from pathlib import Path
from typing import TYPE_CHECKING, Any

from benchmarks.xray_layout.bench.adapters._base import BaseAdapter, LayoutResult

if TYPE_CHECKING:
    import networkx as nx


# Default node bounding box size used both as ``GraphAttributes.setAllWidth``
# /``setAllHeight`` defaults and as the per-node size we emit in
# ``LayoutResult.node_sizes``.  Picked to match libavoid's defaults so cross-
# adapter renders sit at comparable scales.
_DEFAULT_NODE_WIDTH = 50.0
_DEFAULT_NODE_HEIGHT = 30.0

# Repo-relative path to the locally built OGDF source tree.  The ``build``
# subdirectory holds the shared libraries plus the autogenerated headers
# (``include/ogdf-release/ogdf/basic/internal/config_autogen.h``) that
# ``ogdf-python`` looks up via ``$OGDF_BUILD_DIR/OgdfTargets.cmake``.
_OGDF_BUILD_DIR = (
    Path(__file__).resolve().parents[2] / "third_party" / "ogdf" / "build"
)


def _bootstrap_ogdf_env() -> None:
    """Set ``OGDF_BUILD_DIR`` and ``LD_LIBRARY_PATH`` for ogdf-python.

    Must run before the first ``import ogdf_python`` -- the loader reads
    ``OGDF_BUILD_DIR`` at module import time and dlopens ``libOGDF.so`` /
    ``libCOIN.so`` from the runtime linker search path.  We mutate
    ``os.environ`` in-place rather than wrapping the import in a subprocess
    so subsequent calls in the same interpreter reuse the loaded module
    (cppyy cannot be re-initialised cheaply).
    """
    build_dir = str(_OGDF_BUILD_DIR)
    os.environ.setdefault("OGDF_BUILD_DIR", build_dir)
    existing = os.environ.get("LD_LIBRARY_PATH", "")
    parts = existing.split(os.pathsep) if existing else []
    if build_dir not in parts:
        parts.insert(0, build_dir)
        os.environ["LD_LIBRARY_PATH"] = os.pathsep.join(parts)


class OgdfAdapter(BaseAdapter):
    """Adapter wrapping OGDF orthogonal/hierarchical layouts via ogdf-python.

    Stores the most recent layout's notes on the instance so callers can
    inspect which engine ran without re-driving the layout.  ``render()``
    rasterises positions+routes via matplotlib in the same style as the
    other adapters (libavoid, etc.) so cross-adapter PNGs stay comparable.
    """

    name = "ogdf"

    def __init__(self) -> None:
        super().__init__(self.name)
        # Last-run diagnostics; populated by layout(), exposed only for
        # debugging.  Not part of the canonical metrics schema.
        self.notes: dict[str, str] = {}

    def install_check(self) -> tuple[bool, str]:
        """Probe for ``ogdf_python`` with the local OGDF build wired up.

        Sets ``OGDF_BUILD_DIR`` / ``LD_LIBRARY_PATH`` first, then attempts
        the import.  Any failure (missing build dir, missing shared libs,
        missing autogen headers, cppyy interpreter setup error) is reported
        as ``(False, "ogdf_python: <reason>")`` so the harness records a
        clean ``deferred`` run.
        """
        if not _OGDF_BUILD_DIR.exists():
            return (False, f"ogdf_python: OGDF build dir not found at {_OGDF_BUILD_DIR}")
        _bootstrap_ogdf_env()
        try:
            import ogdf_python
        except Exception as exc:  # noqa: BLE001 - cppyy raises diverse types
            return (False, f"ogdf_python: {exc}")
        version = getattr(ogdf_python, "__version__", "unknown")
        return (True, f"ogdf_python {version}")

    def layout(self, graph: "nx.MultiDiGraph[Any]") -> LayoutResult:
        """Lay out *graph* with OGDF, preferring orthogonal planarisation.

        Algorithm:

        1. Bootstrap the OGDF environment and import ``ogdf_python``.  The
           harness only calls ``layout()`` after ``install_check()`` returned
           True, so the import is expected to succeed; any failure here
           bubbles up as a hard error rather than being caught.
        2. Build an ``ogdf.Graph`` mirroring the input nodes/edges, keeping
           a Python-side ``node_handles`` map so we can read back positions
           and a ``edge_handles`` map for bend-route extraction.  Self-loops
           are skipped because OGDF's planarisation pipeline cannot route
           them.
        3. Wrap the graph in a ``GraphAttributes`` with the
           ``nodeGraphics | edgeGraphics`` flag set so positions and bend
           polylines survive the layout call.  ``setAllWidth`` /
           ``setAllHeight`` seed every node with the default bounding box.
           Per-node overrides from ``graph.nodes[id]['width'/'height']`` are
           written through ``GA.width(node, ...)`` style setters so the
           layout respects the caller's sizing.
        4. Run ``PlanarizationLayout`` first.  On any exception (typically a
           non-planar input graph) we tear it down and retry with
           ``SugiyamaLayout``, recording ``notes['layout_engine']`` to mark
           which path ran.
        5. Read positions via ``GA.x(node)`` / ``GA.y(node)`` and edge
           routes by stitching ``[source_centre, *bends, target_centre]``
           into a polyline (OGDF stores only the interior bend points).
        """
        _bootstrap_ogdf_env()
        # Local imports keep module load cheap when the harness only needs
        # to call ``install_check`` (e.g. during ``bench check``).
        from ogdf_python import cppinclude, ogdf

        cppinclude("ogdf/planarity/PlanarizationLayout.h")
        cppinclude("ogdf/layered/SugiyamaLayout.h")

        ordered_nodes: list[object] = sorted(graph.nodes(), key=repr)

        # Per-node sizes also flow into LayoutResult.node_sizes so render()
        # and downstream metrics see the same dimensions OGDF used.
        node_sizes: dict[object, tuple[float, float]] = {}
        for node_id in ordered_nodes:
            attrs = graph.nodes[node_id]
            width = float(attrs.get("width", _DEFAULT_NODE_WIDTH))
            height = float(attrs.get("height", _DEFAULT_NODE_HEIGHT))
            node_sizes[node_id] = (width, height)

        # Build graph + attributes.  We rebuild from scratch on a fallback
        # retry because OGDF's planarisation pipeline mutates the
        # GraphAttributes in ways that interact poorly with re-running a
        # different layout on the same instance.
        def _build() -> tuple[Any, Any, dict[object, Any], dict[tuple[object, object, object], Any]]:
            g = ogdf.Graph()
            node_handles: dict[object, Any] = {}
            for node_id in ordered_nodes:
                node_handles[node_id] = g.newNode()
            edge_handles: dict[tuple[object, object, object], Any] = {}
            for source, target, key in graph.edges(keys=True):
                if source == target:
                    # OGDF orthogonal routing cannot represent a self-loop;
                    # skip rather than synthesise so metrics see the absence.
                    continue
                edge_handles[(source, target, key)] = g.newEdge(
                    node_handles[source], node_handles[target]
                )
            ga = ogdf.GraphAttributes(
                g,
                ogdf.GraphAttributes.nodeGraphics
                | ogdf.GraphAttributes.edgeGraphics,
            )
            ga.setAllWidth(_DEFAULT_NODE_WIDTH)
            ga.setAllHeight(_DEFAULT_NODE_HEIGHT)
            # Apply per-node sizes via the no-arg overloads, which return
            # the underlying ``GraphRegisteredArray<double>`` we can index
            # with the node handle.  The ``GA.width(node)`` single-arg form
            # returns a value copy in cppyy and so cannot be used as an
            # lvalue; the array form is the supported mutation entry point.
            width_arr = ga.width()
            height_arr = ga.height()
            for node_id, (width, height) in node_sizes.items():
                handle = node_handles[node_id]
                width_arr[handle] = width
                height_arr[handle] = height
            return g, ga, node_handles, edge_handles

        # Primary path: orthogonal planarisation.
        engine = "ortho"
        g, ga, node_handles, edge_handles = _build()
        t0 = time.perf_counter()
        try:
            primary = ogdf.PlanarizationLayout()
            # Prevent cppyy from freeing the algorithm object on GC -- OGDF
            # destructors interact poorly with the interpreter shutdown
            # ordering and have produced double-free crashes in testing.
            primary.__python_owns__ = False
            # Note: ogdf_python does not expose OrthoLayout in this build,
            # so we rely on PlanarizationLayout's default planar layouter.
            # If OrthoLayout becomes available, wrap it and call
            # primary.setPlanarLayouter(ortho) here.
            primary.call(ga)
        except Exception:  # noqa: BLE001 - cppyy bubbles C++ exceptions opaquely
            # Planarisation rejected the graph (most often non-planar);
            # rebuild from scratch and run Sugiyama instead.  We do *not*
            # try to recover the partial layout -- the harness contract
            # requires a complete LayoutResult or a hard failure.
            engine = "sugiyama_fallback"
            g, ga, node_handles, edge_handles = _build()
            fallback = ogdf.SugiyamaLayout()
            fallback.__python_owns__ = False
            fallback.call(ga)
        runtime_ms = (time.perf_counter() - t0) * 1000.0

        self.notes = {"layout_engine": engine}

        # Read back positions.  OGDF stores node coordinates as the centre
        # point of each node's bounding box, which matches the convention
        # used by the other adapters and by xray's renderer.
        node_positions: dict[object, tuple[float, float]] = {}
        for node_id, handle in node_handles.items():
            node_positions[node_id] = (float(ga.x(handle)), float(ga.y(handle)))

        # Stitch edge polylines.  GA.bends(edge) yields only the interior
        # bend points; we sandwich them between the source/target centres
        # so downstream metrics see a complete polyline per edge.
        edge_routes: dict[tuple[object, object, object], list[tuple[float, float]]] = {}
        for edge_id, handle in edge_handles.items():
            source, target, _key = edge_id
            polyline: list[tuple[float, float]] = [node_positions[source]]
            try:
                bends = ga.bends(handle)
                for pt in bends:
                    polyline.append((float(pt.m_x), float(pt.m_y)))
            except Exception:  # noqa: BLE001 - defensive: bends iter may raise
                # If bend extraction fails for an individual edge we keep
                # the straight source->target segment rather than aborting
                # the whole layout; this matches libavoid_adapter's defence
                # for connectors with no display route.
                pass
            polyline.append(node_positions[target])
            edge_routes[edge_id] = polyline

        # Drop the algorithm objects before returning so the next layout()
        # invocation on the same adapter starts fresh.  We do not call into
        # OGDF here -- ``__python_owns__ = False`` already detached the
        # algorithm objects from cppyy's GC.
        return LayoutResult(
            node_positions=node_positions,
            edge_routes=edge_routes,
            runtime_ms=runtime_ms,
            node_sizes=node_sizes,
        )

    def render(
        self,
        result: LayoutResult,
        graph: "nx.MultiDiGraph[Any]",
        out_path: Path,
    ) -> None:
        """Rasterise the OGDF layout to PNG using matplotlib.

        Mirrors libavoid_adapter.render() so cross-adapter PNGs sit at the
        same visual scale: filled rectangles for nodes, polylines for edges,
        equal aspect ratio with an inverted Y axis (screen-style).  The
        graph argument is intentionally unused; ``LayoutResult`` already
        carries every coordinate we need.
        """
        _ = graph  # see docstring; results carry all geometry we need
        # Local imports keep module load cheap and let install_check() run
        # without pulling matplotlib into the cppyy interpreter context.
        import matplotlib

        matplotlib.use("Agg")  # ensure no DISPLAY-dependent backend kicks in
        import matplotlib.patches as mpatches
        import matplotlib.pyplot as plt

        out_path.parent.mkdir(parents=True, exist_ok=True)

        if not result.node_positions:
            # Empty layouts still need a placeholder PNG so the harness
            # finds the artifact it expects on disk.
            fig, _ax = plt.subplots(figsize=(1, 1), dpi=100)
            fig.savefig(str(out_path), format="png")
            plt.close(fig)
            return

        xs = [p[0] for p in result.node_positions.values()]
        ys = [p[1] for p in result.node_positions.values()]
        widths = [s[0] for s in result.node_sizes.values()]
        heights = [s[1] for s in result.node_sizes.values()]
        margin_x = max(widths) if widths else 1.0
        margin_y = max(heights) if heights else 1.0
        x_min = min(xs) - margin_x
        x_max = max(xs) + margin_x
        y_min = min(ys) - margin_y
        y_max = max(ys) + margin_y

        # Match libavoid_adapter's canvas sizing so PNG diff comparisons
        # across adapters stay meaningful.
        canvas_width = max(4.0, min(20.0, (x_max - x_min) / 100.0))
        canvas_height = max(4.0, min(20.0, (y_max - y_min) / 100.0))

        fig, ax = plt.subplots(figsize=(canvas_width, canvas_height), dpi=120)
        ax.set_xlim(x_min, x_max)
        # Invert Y so screen-style coordinates (y grows downward) match the
        # other adapters' rendering convention.
        ax.set_ylim(y_max, y_min)
        ax.set_aspect("equal")
        ax.axis("off")

        # Edges first, so node rectangles paint on top of edge endpoints.
        for route in result.edge_routes.values():
            if len(route) < 2:
                continue
            edge_xs = [float(pt[0]) for pt in route]
            edge_ys = [float(pt[1]) for pt in route]
            ax.plot(
                edge_xs,
                edge_ys,
                color="#444444",
                linewidth=1.0,
                solid_capstyle="round",
                solid_joinstyle="miter",
            )

        for node_id, (cx, cy) in result.node_positions.items():
            width, height = result.node_sizes.get(
                node_id, (_DEFAULT_NODE_WIDTH, _DEFAULT_NODE_HEIGHT)
            )
            rect = mpatches.Rectangle(
                (cx - width / 2.0, cy - height / 2.0),
                width,
                height,
                linewidth=1.0,
                edgecolor="#222222",
                facecolor="#cfe2ff",
            )
            ax.add_patch(rect)

        fig.savefig(str(out_path), format="png", bbox_inches="tight")
        plt.close(fig)
